collections=$(ls -d */ | cut -f1 -d'/')

    for collection in $collections; do
		echo
		echo "#############################################################################################"
		echo "################  "$(basename `pwd`)" - collection: " $collection
		echo "#############################################################################################"
		cd $collection
		for name in $(ls -d */ | cut -f1 -d'/'); do
			cp ../build-* $name
			echo "#############################################################################################"
			echo "################  Copied over the build script to "$name
			echo "#############################################################################################"
		done
		cd ..

	done
	echo "#############################################################################################"
	echo "###################                 Script finished                      ####################"
	echo "#############################################################################################"
